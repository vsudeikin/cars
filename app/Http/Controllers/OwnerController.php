    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
       $owner = Owner::find($id);
       if ($owner->cars->count() > 0) {
            foreach ($owner->cars as $car) {
                $car->owner_id = null;
                $car->save();
            }
       }
       $owner->destroy($id);

       return redirect()->route('main', 1);
    }
